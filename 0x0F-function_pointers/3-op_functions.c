#include "3-calc.h"

/**
 * op_add - function that sum two digits
 * @a: first integer number
 * @b: second integer number
 *
 * Return: sum of two numbers (integer)
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function that get the difference of two numbers
 * @a: first integer number
 * @b: second integer number
 *
 * Return: the difference of two numbers (integer)
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function that get the multiplication of two numbers
 * @a: first integer number
 * @b: second integer number
 *
 * Return: the multiplication of two numbers (integer)
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function that get the division of two numbers
 * @a: first integer number
 * @b: second integer number
 *
 * Return: the division  of two numbers (integer)
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function that get the remainder of two numbers
 * @a: first integer number
 * @b: second integer number
 *
 * Return: the remainder of two numbers (integer)
 */

int op_mod(int a, int b)
{
	return (a % b);
}
